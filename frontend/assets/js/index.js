function runPageScript(pageName) {
  switch (pageName) {
    case 'Employee':
      fetch(`${baseURL}/employee`)
        .then(res => res.json())
        .then(data => {
          const list = document.getElementById('employee-list');
          if (!list) return;

          list.innerHTML = '';
          if (data.length === 0) {
            list.innerHTML = '<li>‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏û‡∏ô‡∏±‡∏Å‡∏á‡∏≤‡∏ô</li>';
            return;
          }

          data.forEach(emp => {
            const li = document.createElement('li');
            li.textContent = `${emp.employeeId} - ${emp.employeeName} (${emp.department})`;
            list.appendChild(li);
          });
        })
        .catch(err => {
          console.error('‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏û‡∏ô‡∏±‡∏Å‡∏á‡∏≤‡∏ô‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß:', err);
          const list = document.getElementById('employee-list');
          if (list) {
            list.innerHTML = `<li style="color:red;">‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•</li>`;
          }
        });
      break;

    // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏´‡∏ô‡πâ‡∏≤‡∏≠‡∏∑‡πà‡∏ô ‡πÄ‡∏ä‡πà‡∏ô Uniform, Wash ‡∏ï‡πà‡∏≠‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏¢
  }
}

